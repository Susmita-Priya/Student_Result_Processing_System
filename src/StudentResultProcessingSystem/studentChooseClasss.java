    private void jButtonAllreaultActionPerformed(java.awt.event.ActionEvent evt) {                                                 
        // TODO add your handling code here:
          try {
            // TODO add your handling code here:
            Class.forName("com.mysql.cj.jdbc.Driver");
            String user="root";
            String password="";
            String url ="jdbc:mysql://127.0.0.1:3306/student?serverTimezone=UTC";
            Connection conn = DriverManager.getConnection(url, user, password);
            Statement statement = conn.createStatement();
            String query="SELECT id, dld, java, math, accounting, electronics, stasitics, total FROM studentresultt ";
            ResultSet result= statement.executeQuery(query);
            String finalmsg="";
            while(result.next())
            {
                String id,d,j,m,a,e,s,t;
                id=result.getString("id");
                d=result.getString("dld");
                j=result.getString("java");
                m=result.getString("math");
                a=result.getString("accounting");
                e=result.getString("electronics");
                s=result.getString("stasitics");
                t=result.getString("total");
                String msg = "ID= "+id+"   DLD= "+d+"   JAVA= "+j+"   MATH= "+m+"   ACCOUNTING= "+a+"   ELECTRONICS= "+e+"   STASITICS= "+s+"   Total= "+t+"\n\n";
                finalmsg=finalmsg+msg;
            }
            JOptionPane.showMessageDialog(studentChooseClasss.this, finalmsg);
            statement.close();
            conn.close();
            
            
            
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(AdminChooseClass.class.getName()).log(Level.SEVERE, null, ex);
        }
    }                                                

