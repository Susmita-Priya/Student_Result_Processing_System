    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            // TODO add your handling code here:
            Class.forName("com.mysql.cj.jdbc.Driver");
            String user="root";
            String password="";
            String url ="jdbc:mysql://127.0.0.1:3306/student?serverTimezone=UTC";
            Connection conn = DriverManager.getConnection(url, user, password);
            Statement statement = conn.createStatement();

            String s1=jTextFieldId.getText();
            int id=Integer.parseInt(s1);
            String query ="SELECT * FROM attendancee WHERE id = "+id;
            PreparedStatement pst= conn.prepareStatement(query);
            ResultSet result= pst.executeQuery();

            if(result.next())
            {
                //String paw=result.getString("Password");
                String d,j,m,a,e,s,t;
                d=result.getString("dld");
                j=result.getString("java");
                m=result.getString("math");
                a=result.getString("accounting");
                e=result.getString("electronics");
                s=result.getString("stasitics");
                t=result.getString("total");

                jTextFieldDld.setText(d);
                jTextFieldJava.setText(j);
                jTextFieldMath.setText(m);
                jTextFieldAccounting.setText(a);
                jTextFieldElectronics.setText(e);
                jTextFieldStasitics.setText(s);
                jTextFieldTotal.setText(t);
            }

            statement.close();
            conn.close();
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(StudentResultClass.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

